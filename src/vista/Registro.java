    private void lblEsAdministradorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblEsAdministradorMouseClicked
        
        lblWarning.setText("");
        String claveIngresada;
        int intentos = 3;

        try {

            do {

                claveIngresada = JOptionPane.showInputDialog(this, "Ingrese clave de administrador",
                        "Clave", JOptionPane.QUESTION_MESSAGE);

                if (claveIngresada.equals(claveAdmin)) {

                    lblEsAdministrador.setVisible(false);
                    rol = 1;

                } else if (!claveIngresada.equals(claveAdmin)) {
                    intentos--;
                    JOptionPane.showMessageDialog(this, "Clave incorrecta, quedan " + intentos + " intentos");
                }

                if (intentos == 0) {
                    break;
                }

            } while (!claveIngresada.equals(claveAdmin));

        } catch (NullPointerException e) {

        }


    }//GEN-LAST:event_lblEsAdministradorMouseClicked

