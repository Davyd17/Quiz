    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        if (!txtNombreUsuario.getText().isEmpty() && !txtContrasena.getText().isEmpty()) {

            String resultado = new UsuarioControl().registrar(txtNombreUsuario.getText(), txtContrasena.getText(), rol);

            if (resultado.equals(resultado)) {
                lblWarning.setText("Se ha registrado exitosamente");
                txtNombreUsuario.setText("");
                txtContrasena.setText("");
            } else {
                lblWarning.setText(resultado);
            }

        } else {
            lblWarning.setText("Todos los campos son obligatorios");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

